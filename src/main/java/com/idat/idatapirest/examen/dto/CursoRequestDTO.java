package com.idat.idatapirest.examen.dto;

public class CursoRequestDTO {
	private Integer idResquest;
	private String nombreCurso;
	private String descripcionCurso;
	private Integer idMallaCurso;
	public Integer getIdResquest() {
		return idResquest;
	}
	public void setIdResquest(Integer idResquest) {
		this.idResquest = idResquest;
	}
	public String getNombreCurso() {
		return nombreCurso;
	}
	public void setNombreCurso(String nombreCurso) {
		this.nombreCurso = nombreCurso;
	}
	public String getDescripcionCurso() {
		return descripcionCurso;
	}
	public void setDescripcionCurso(String descripcionCurso) {
		this.descripcionCurso = descripcionCurso;
	}
	public Integer getIdMallaCurso() {
		return idMallaCurso;
	}
	public void setIdMallaCurso(Integer idMallaCurso) {
		this.idMallaCurso = idMallaCurso;
	}
	
	
	
	
	
}
